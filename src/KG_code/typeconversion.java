package KG_code;

public class typeconversion {
    public static void main(String[] args) {
        // imolicit conversion(auto converion)
        float myfloat= 5;
        System.out.println(myfloat);

        // explicit convertion (user convert)
        int myInt = (int)5.0f;
        System.out.println(myInt);
    }
}
